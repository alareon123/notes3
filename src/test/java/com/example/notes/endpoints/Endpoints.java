package com.example.notes.endpoints;

/**
 * Класс для хранения URL-адресов (эндпоинтов) API.
 *
 * ЗАЧЕМ НУЖЕН ЭТОТ КЛАСС:
 * Вместо того, чтобы писать строки с URL в каждом тесте, мы храним их в одном месте.
 * Это удобно, потому что если URL изменится, нам нужно будет изменить его только здесь.
 *
 * ПРИМЕР БЕЗ ЭТОГО КЛАССА (плохо):
 *   get("/notes") // в первом тесте
 *   get("/notes") // во втором тесте
 *   get("/notes") // в третьем тесте
 *   Если URL изменится на "/api/notes", нужно менять во всех местах!
 *
 * ПРИМЕР С ЭТИМ КЛАССОМ (хорошо):
 *   get(Endpoints.NOTES) // во всех тестах используем константу
 *   Если URL изменится, меняем только в одном месте - здесь!
 *
 * ЧТО ТАКОЕ ЭНДПОИНТ (endpoint):
 * Это путь в URL, по которому доступен определённый ресурс API.
 * Например: http://localhost:8080/notes
 *           └─────────база────────┘└──эндпоинт──┘
 */
public class Endpoints {

    /**
     * Эндпоинт для работы со списком всех заметок.
     *
     * ИСПОЛЬЗУЕТСЯ ДЛЯ:
     * - GET /notes - получить все заметки
     * - POST /notes - создать новую заметку
     *
     * Значение: "/notes"
     */
    public static final String NOTES = "/notes";

    /**
     * Эндпоинт для работы с конкретной заметкой по её ID.
     *
     * ЧТО ТАКОЕ {id}:
     * Это placeholder (заполнитель), который будет заменён на реальный ID заметки.
     * Например: "/notes/{id}" превратится в "/notes/123"
     *
     * ИСПОЛЬЗУЕТСЯ ДЛЯ:
     * - GET /notes/{id} - получить заметку с определённым ID
     * - PUT /notes/{id} - обновить заметку с определённым ID
     * - DELETE /notes/{id} - удалить заметку с определённым ID
     *
     * Значение: "/notes/{id}"
     */
    public static final String NOTES_BY_ID = "/notes/{id}";

    /**
     * Вспомогательный метод для создания URL заметки с конкретным ID.
     *
     * ЗАЧЕМ НУЖЕН:
     * Этот метод заменяет placeholder {id} на реальное значение ID.
     *
     * ПРИМЕРЫ ИСПОЛЬЗОВАНИЯ:
     *   noteById("123")  вернёт  "/notes/123"
     *   noteById("abc")  вернёт  "/notes/abc"
     *
     * КАК РАБОТАЕТ:
     * 1. Берёт строку "/notes/{id}"
     * 2. Заменяет "{id}" на переданное значение
     * 3. Возвращает готовую строку
     *
     * @param id идентификатор заметки (может быть числом или строкой)
     * @return готовый URL вида "/notes/123"
     */
    public static String noteById(String id) {
        // Метод replace() заменяет первый параметр ("{id}") на второй (значение id)
        return NOTES_BY_ID.replace("{id}", id);
    }
}
